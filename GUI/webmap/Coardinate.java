public class Coardinate {
    double lng;
    double lat;
    
    public Coardinate(double lat, double lon){
        this.lng = lng;
        this.lat = lat;
    }
    
    public double getLat(){
        return lat;
    }
    
    public double getLng(){
        return lng;
    }
    
    
}
